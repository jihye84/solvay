async def generate(name: str, text: str) -> None:
    out_path = os.path.join(OUTPUT_DIR, f"{name}.mp3")
    communicate = edge_tts.Communicate(text, VOICE, rate=RATE)
    await communicate.save(out_path)
    print(f"  ✓ {out_path}")


async def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    print(f"[TTS] {len(scientists)}개 파일 생성 중... (음성: {VOICE})\n")
    for name, text in scientists:
        await generate(name, text)
    print(f"\n[완료] audio/ 폴더에 {len(scientists)}개 MP3 파일이 생성되었습니다.")

asyncio.run(main())
